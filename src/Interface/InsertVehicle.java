    private void jBtn_InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_InsertActionPerformed
        // TODO add your handling code here:
        try {
            
            if(carfile.findCar(Integer.parseInt(jTf_SerialNumber.getText())) == false){
            Insert();
            JOptionPane.showMessageDialog(null, "The car has been succesfully entered");
            jTf_Mileage.setText("");
            jTf_Name.setText("");
            jTf_SerialNumber.setText("");
            jTf_Year.setText("");
            }
            else{
                JOptionPane.showMessageDialog(null, "This car already exists", "Error", JOptionPane.ERROR_MESSAGE);
                jTf_Mileage.setText("");
                jTf_Name.setText("");
                jTf_SerialNumber.setText("");
                jTf_Year.setText("");
            }
        } catch (IOException ex) {
            Logger.getLogger(InsertVehicle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBtn_InsertActionPerformed

